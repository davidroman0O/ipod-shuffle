// BuildITunesSD builds the complete iTunesSD database for the given tracks and
// playlists. With zero tracks it returns the fixed empty database. Otherwise it
// emits the 64-byte "bdhs" header (version 0x02000003), the "hths" track
// header + records, and the "hphs" playlist header (master + named). Mirrors
// buildITunesSD in ipodDatabase.ts.
func BuildITunesSD(tracks []Track, playlists []Playlist) []byte {
	if len(tracks) == 0 {
		return buildEmptyDatabase()
	}

	trackRecords := make([][]byte, len(tracks))
	for i, t := range tracks {
		trackRecords[i] = buildTrackRecord(t)
	}
	trackHeader := buildTrackHeader(trackRecords)

	trackIndexByID := make(map[string]uint32, len(tracks))
	for i, t := range tracks {
		trackIndexByID[t.TrackID] = uint32(i)
	}
	resolved := resolvePlayablePlaylists(playlists, trackIndexByID)

	playlistHeader := buildPlaylistHeader(len(tracks), resolved, 64+uint32(len(trackHeader)))

	w := &binaryWriter{}
	w.writeFixedUtf8("bdhs", 4)
	w.writeU32(0x02000003)
	w.writeU32(64)
	w.writeU32(uint32(len(tracks)))
	w.writeU32(uint32(len(resolved) + 1))
	w.writeU64(0)
	w.writeU8(0)
	w.writeU8(0)
	w.writeU16(0)
	w.writeU32(uint32(len(tracks)))
	w.writeU32(64)
	w.writeU32(64 + uint32(len(trackHeader)))
	w.writeBytes(make([]byte, 20))
	w.writeBytes(trackHeader)
	w.writeBytes(playlistHeader)
	return w.bytes()
}
